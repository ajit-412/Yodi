package com.ajit.yodi.Model;

public class POption {

    int image;
    String head;


    public POption(int image, String head) {
        this.image = image;
        this.head = head;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }
}
